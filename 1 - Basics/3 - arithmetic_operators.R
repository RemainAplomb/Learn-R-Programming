# Logical Operators

## Arithmetic Operation
##    - Addition +
##    - Subtraction - 
##    - Multiplication *
##    - Division /
##    - Remainder/modulus %%
##    - Exponent **

## Order of Operation:
##    - Parenthesis ()
##    - Exponent ^
##    - Multiplication and Division (Left to right)
##    - Addition and Subtraction (Left to right)

#===============================================================

## Adding Numeric/Double
n1 <- 20
n2 <- 30
sum <- n1 + n2
sum
typeof(sum)

#===============================================================

## Subtracting integers will result to double
n1 <- 2L
n2 <- 10L
subtracted <- n1 - n2
subtracted
typeof(subtracted)

#===============================================================

## Multiplying Numeric/Double
n1 <- 20
n2 <- 30
product <- n1 * n2
product
typeof(product)

#===============================================================

## Dividing
n1 <- 20
n2 <- 5
divided <- n1 / n2
divided
typeof(divided)

#===============================================================

## Remainder/Modulus
n1 <- 21
n2 <- 5
remainder <- n1 %% n2
remainder
typeof(remainder)

#===============================================================

## Exponent
n1 <- 2
n2 <- 5
exponent <- n1 ** n2
exponent
typeof(exponent)

