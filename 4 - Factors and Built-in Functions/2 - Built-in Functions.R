#===============================================================

# Built-in Functions

#===============================================================

## Mean
vector <- c(2, 4, 6, 8, 10)
mean_value <- mean(vector)
print(mean_value)  # Output: 6


#===============================================================

## Vector
vector <- c(1, 2, 3, 4, 5)
sum_value <- sum(vector)
print(sum_value)  # Output: 15

#===============================================================

## Length
vector <- c(1, 2, 3, 4, 5)
length_value <- length(vector)
print(length_value)  # Output: 5

#===============================================================

## Min
vector <- c(5, 3, 9, 2, 7)
min_value <- min(vector)
print(min_value)  # Output: 2

#===============================================================

## Max
vector <- c(5, 3, 9, 2, 7)
max_value <- max(vector)
print(min_value)  # Output: 2

#===============================================================

## Sort
vector <- c(5, 3, 9, 2, 7)
sorted_vector <- sort(vector)
print(sorted_vector)  # Output: 2 3 5 7 9

#===============================================================

## Reverse
vector <- c(1, 2, 3, 4, 5)
reversed_vector <- rev(vector)
print(reversed_vector)  # Output: 5 4 3 2 1

#===============================================================

## Unique
vector <- c(1, 2, 3, 2, 4, 1, 5)
unique_values <- unique(vector)
print(unique_values)  # Output: 1 2 3 4 5

#===============================================================

## Paste
string1 <- "Hello"
string2 <- "World"
result <- paste(string1, string2)
print(result)  # Output: "Hello World"

#===============================================================

## Substring
string <- "Hello World"
substring <- substr(string, start = 7, stop = 11)
print(substring)  # Output: "World"

