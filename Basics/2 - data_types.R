# Data Types
#   - Numeric/Double 20
#   - Integer 20L
#   - String/Character "Hello World!"
#   - Complex
#   - Raw

## Numeric Real Number
numeric_variable <- 20
print(numeric_variable)
typeof(numeric_variable)
class(numeric_variable)

## Numeric Floating Point
float_variable <- 2.25
print(float_variable)
typeof(float_variable)

## Integer
integer_variable <- 20L
print(integer_variable)
typeof(integer_variable)
class(integer_variable)

## String/Character
string_variable <- "What's up?"
print(string_variable)
typeof(string_variable)
class(string_variable)

## Complex
complex_variable <- 3 + 2i
print(complex_variable)
typeof(complex_variable)
class(complex_variable)

## Raw
raw_variable <- charToRaw("This will be converted to raw")
print(raw_variable)
typeof(raw_variable)
class(raw_variable)

